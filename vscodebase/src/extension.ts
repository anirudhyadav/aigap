function registerChatParticipant(context: vscode.ExtensionContext): void {
  if (!("chat" in vscode)) return;

  const handler: vscode.ChatRequestHandler = async (
    request: vscode.ChatRequest,
    _chatContext: vscode.ChatContext,
    stream: vscode.ChatResponseStream,
    _token: vscode.CancellationToken
  ) => {
    const root = getWorkspaceRoot();
    if (!root) {
      stream.markdown("No workspace folder open. Open a project first.");
      return;
    }

    const aigapDir = getAigapDir(root);
    const policiesPath = path.join(aigapDir, "POLICIES.md");
    const policiesContent = fs.existsSync(policiesPath)
      ? fs.readFileSync(policiesPath, "utf-8")
      : "(POLICIES.md not found — run aigap: Initialize first)";

    const userQuery = request.prompt.toLowerCase();

    if (userQuery.includes("gap") || userQuery.includes("unenforced")) {
      stream.markdown("Running gap scan...");
      await vscode.commands.executeCommand("aigap.gapReport");
      stream.markdown("Gap report written to `.aigap/gap-report.md`. Open it to see unenforced policies.");
    } else if (userQuery.includes("status") || userQuery.includes("compliance")) {
      const gpIds = (policiesContent.match(/GP-\d{3}/g) || []);
      const enforced = (policiesContent.match(/✅ enforced/g) || []).length;
      stream.markdown(
        `**Policy Status**\n\n- Policies defined: ${gpIds.length}\n- Enforced: ${enforced}\n- Gaps: ${gpIds.length - enforced}`
      );
    } else if (userQuery.includes("task") || userQuery.includes("sprint")) {
      stream.markdown("Generating sprint feed from gap report...");
      await vscode.commands.executeCommand("aigap.sprintFeed");
    } else {
      stream.markdown(
        `**aigap** — AI Guardrails and Policies\n\nI can help you with:\n- \`@aigap gaps\` — list unenforced policies\n- \`@aigap status\` — compliance overview\n- \`@aigap task GP-NNN\` — generate a sprint task\n\nOr ask in plain English: *"What policies are unenforced?"*`
      );
    }
  };

  const participant = (vscode as unknown as { chat: { createChatParticipant: (id: string, handler: vscode.ChatRequestHandler) => vscode.ChatParticipant } }).chat.createChatParticipant(
    "aigap.participant",
    handler
  );
  participant.iconPath = new vscode.ThemeIcon("shield");
  context.subscriptions.push(participant);
}

