export function activate(context: vscode.ExtensionContext): void {
  const commands: [string, () => Promise<void>][] = [
    ["aigap.init", cmdInit],
    ["aigap.update", cmdUpdate],
    ["aigap.validate", cmdValidate],
    ["aigap.gapReport", cmdGapReport],
    ["aigap.enforcement", cmdEnforcement],
    ["aigap.auditReport", cmdAuditReport],
    ["aigap.changeImpact", cmdChangeImpact],
    ["aigap.frameworkMap", cmdFrameworkMap],
    ["aigap.sprintFeed", cmdSprintFeed],
    ["aigap.prDraft", cmdPrDraft],
    ["aigap.releaseNotes", cmdReleaseNotes],
    ["aigap.statusReport", cmdStatusReport],
    ["aigap.staleness", cmdStaleness],
    ["aigap.testLinkage", cmdTestLinkage],
    ["aigap.ingestConfluence", cmdIngestConfluence],
  ];

  for (const [id, handler] of commands) {
    context.subscriptions.push(
      vscode.commands.registerCommand(id, async () => {
        try {
          await handler();
        } catch (err) {
          if (err instanceof Error && err.message !== "No workspace") {
            showError(err.message);
          }
        }
      })
    );
  }

  registerChatParticipant(context);
}

