async function cmdInit(): Promise<void> {
  const root = requireWorkspace();
  const fileUri = await pickFile("Select policy document");
  if (!fileUri) return;

  const aigapDir = ensureAigapDir(root);
  const registry = getRegistry(aigapDir);

  const policiesPath = path.join(aigapDir, "POLICIES.md");
  const date = new Date().toISOString().split("T")[0];

  // Stub — in production this calls Copilot LLM on the file contents
  const stub = `# POLICIES — ${path.basename(root)}
<!-- aigap v0.1.0 · generated by aigap.init · ${date} -->

version: 0.1.0
updated: ${date}
author: Anirudh Yadav

---

## Summary

| Type | Count |
|---|---|
| Guardrail Categories (GC) | 0 |
| Guardrail Policies (GP) | 0 |
| Enforcement Vectors (EV) | 0 |

---

> Edit this file or re-run **aigap: Initialize from Policy Doc** with your full policy document.
> Source: ${fileUri.fsPath}
`;

  fs.writeFileSync(policiesPath, stub);
  saveRegistry(aigapDir, registry);
  openDocument(policiesPath);
  showInfo(`Initialized .aigap/POLICIES.md from ${path.basename(fileUri.fsPath)}`);
}

async function cmdUpdate(): Promise<void> {
  const root = requireWorkspace();
  const aigapDir = ensureAigapDir(root);
  const policiesPath = path.join(aigapDir, "POLICIES.md");

  if (!fs.existsSync(policiesPath)) {
    showError("POLICIES.md not found. Run aigap: Initialize first.");
    return;
  }

  const name = await vscode.window.showInputBox({ prompt: "Guardrail policy name (e.g. No PII in prompt)" });
  if (!name) return;

  const description = await vscode.window.showInputBox({ prompt: "Description (1–2 sentences, precise and enforceable)" });
  if (!description) return;

  const severity = await vscode.window.showQuickPick(["critical", "high", "medium", "low"], { placeHolder: "Severity" });
  if (!severity) return;

  const registry = getRegistry(aigapDir);
  const gpId = nextId("GP", registry);
  const date = new Date().toISOString().split("T")[0];

  const entry = `\n| ${gpId} | ${name} | ${description} | ${severity} | — | 🔲 gap |\n`;
  fs.appendFileSync(policiesPath, entry);
  saveRegistry(aigapDir, registry);

  showInfo(`${gpId} added to POLICIES.md`);
  vscode.commands.executeCommand("aigap.validate");
}

async function cmdValidate(): Promise<void> {
  const root = requireWorkspace();
  const aigapDir = getAigapDir(root);
  const policiesPath = path.join(aigapDir, "POLICIES.md");

  if (!fs.existsSync(policiesPath)) {
    showError("POLICIES.md not found. Run aigap: Initialize first.");
    return;
  }

  const content = fs.readFileSync(policiesPath, "utf-8");
  const idPattern = /GP-(\d{3})/g;
  const ids: string[] = [];
  let match: RegExpExecArray | null;

  while ((match = idPattern.exec(content)) !== null) {
    ids.push(match[0]);
  }

  const duplicates = ids.filter((id, i) => ids.indexOf(id) !== i);
  if (duplicates.length > 0) {
    showError(`Duplicate IDs found: ${[...new Set(duplicates)].join(", ")}`);
    return;
  }

  // Write traceability index stub
  const indexPath = path.join(aigapDir, "index.md");
  const date = new Date().toISOString().split("T")[0];
  const indexContent = `# Policy Traceability Index
<!-- updated: ${date} -->

| ID | Name | File | Test | Status |
|---|---|---|---|---|
${ids.map((id) => `| ${id} | — | — | — | 🔲 gap |`).join("\n")}
`;
  fs.writeFileSync(indexPath, indexContent);
  showInfo(`POLICIES.md valid — ${ids.length} policies, index.md updated`);
}

async function cmdGapReport(): Promise<void> {
  const root = requireWorkspace();
  const aigapDir = ensureAigapDir(root);
  const editor = vscode.window.activeTextEditor;

  const target = editor ? editor.document.fileName : root;
  const label = editor ? path.basename(target) : "workspace";

  // Scan for GP-NNN references in the target file / workspace
  const grepResult = cp.spawnSync("grep", ["-rn", "GP-[0-9]\\{3\\}", target], { encoding: "utf-8" });
  const references = grepResult.stdout || "(no GP-NNN references found)";

  const gapPath = path.join(aigapDir, "gap-report.md");
  const date = new Date().toISOString().split("T")[0];

  const content = `# Gap Report — ${label}
<!-- aigap · ${date} -->

## GP-NNN references found

\`\`\`
${references}
\`\`\`

## Cross-reference with POLICIES.md

Run \`aigap check . --policy .aigap-policy.yaml\` for full enforcement gap analysis.
`;
  fs.writeFileSync(gapPath, content);
  openDocument(gapPath);
  showInfo("Gap report written to .aigap/gap-report.md");
}

async function cmdEnforcement(): Promise<void> {
  const root = requireWorkspace();
  const aigapDir = ensureAigapDir(root);
  const enforcementDir = path.join(aigapDir, "enforcement");
  if (!fs.existsSync(enforcementDir)) fs.mkdirSync(enforcementDir, { recursive: true });

  const language = await vscode.window.showQuickPick(["Python", "TypeScript", "Java"], {
    placeHolder: "Target language for enforcement stubs",
  });
  if (!language) return;

  const ext = language === "Python" ? "py" : language === "TypeScript" ? "ts" : "java";
  const stubPath = path.join(enforcementDir, `enforcement_stubs.${ext}`);

  const stubContent =
    language === "Python"
      ? `# aigap enforcement stubs — generated ${new Date().toISOString().split("T")[0]}
# Add GP-NNN IDs to implement specific policies

def pre_call_hook(prompt: str) -> str:
    """EV-001: pre-call hook — runs before every LLM call."""
    # GP-001: No PII in prompt
    _check_no_pii(prompt)
    return prompt

def output_filter(response: str) -> str:
    """EV-002: output filter — runs after every LLM response."""
    return response

def _check_no_pii(text: str) -> None:
    """GP-001 enforcement: raise if PII detected."""
    import re
    patterns = [r"\\b\\d{3}-\\d{2}-\\d{4}\\b", r"\\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Z|a-z]{2,}\\b"]
    for pattern in patterns:
        if re.search(pattern, text):
            raise ValueError(f"GP-001 violation: PII detected in text")
`
      : `// aigap enforcement stubs — generated ${new Date().toISOString().split("T")[0]}
// Add GP-NNN IDs to implement specific policies

export function preCallHook(prompt: string): string {
  // EV-001: pre-call hook
  checkNoPii(prompt); // GP-001
  return prompt;
}

export function outputFilter(response: string): string {
  // EV-002: output filter
  return response;
}

function checkNoPii(text: string): void {
  const ssnPattern = /\\b\\d{3}-\\d{2}-\\d{4}\\b/;
  if (ssnPattern.test(text)) {
    throw new Error("GP-001 violation: PII detected in text");
  }
}
`;

  fs.writeFileSync(stubPath, stubContent);
  openDocument(stubPath);
  showInfo(`Enforcement stubs written to .aigap/enforcement/enforcement_stubs.${ext}`);
}

