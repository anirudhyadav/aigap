import * as vscode from "vscode";
import * as path from "path";
import * as fs from "fs";
import * as cp from "child_process";

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

interface Registry {
  GP: number;
  GC: number;
  EV: number;
  FR: number;
  lastUpdated: string;
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function getAigapDir(workspaceRoot: string): string {
  return path.join(workspaceRoot, ".aigap");
}

function getRegistry(aigapDir: string): Registry {
  const registryPath = path.join(aigapDir, "registry.json");
  if (!fs.existsSync(registryPath)) {
    return { GP: 0, GC: 0, EV: 0, FR: 0, lastUpdated: new Date().toISOString().split("T")[0] };
  }
  return JSON.parse(fs.readFileSync(registryPath, "utf-8")) as Registry;
}

function saveRegistry(aigapDir: string, registry: Registry): void {
  registry.lastUpdated = new Date().toISOString().split("T")[0];
  fs.writeFileSync(path.join(aigapDir, "registry.json"), JSON.stringify(registry, null, 2));
}

function nextId(type: keyof Omit<Registry, "lastUpdated">, registry: Registry): string {
  registry[type] += 1;
  return `${type}-${String(registry[type]).padStart(3, "0")}`;
}

function getWorkspaceRoot(): string | undefined {
  return vscode.workspace.workspaceFolders?.[0]?.uri.fsPath;
}

function requireWorkspace(): string {
  const root = getWorkspaceRoot();
  if (!root) {
    vscode.window.showErrorMessage("aigap: No workspace folder open.");
    throw new Error("No workspace");
  }
  return root;
}

function ensureAigapDir(root: string): string {
  const dir = getAigapDir(root);
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
  return dir;
}

function openDocument(filePath: string): void {
  vscode.workspace.openTextDocument(filePath).then((doc) => {
    vscode.window.showTextDocument(doc);
  });
}

function showInfo(msg: string): void {
  vscode.window.showInformationMessage(`aigap: ${msg}`);
}

function showError(msg: string): void {
  vscode.window.showErrorMessage(`aigap: ${msg}`);
}

async function pickFile(label: string): Promise<vscode.Uri | undefined> {
  const uris = await vscode.window.showOpenDialog({
    canSelectMany: false,
    openLabel: label,
    filters: { "Policy Documents": ["md", "txt", "pdf", "docx"] },
  });
  return uris?.[0];
}

