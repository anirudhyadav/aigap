async function cmdStaleness(): Promise<void> {
  const root = requireWorkspace();
  const aigapDir = getAigapDir(root);
  const policiesPath = path.join(aigapDir, "POLICIES.md");

  if (!fs.existsSync(policiesPath)) {
    showError("POLICIES.md not found. Run aigap: Initialize first.");
    return;
  }

  const content = fs.readFileSync(policiesPath, "utf-8");
  const ids = [...content.matchAll(/GP-(\d{3})/g)].map((m) => m[0]);

  const panel = vscode.window.createWebviewPanel("aigapStaleness", "aigap: Policy Staleness", vscode.ViewColumn.One, {});

  const rows = ids
    .map((id) => {
      const gitLog = cp.spawnSync("git", ["log", "--oneline", `-5`, `--all`, `-S`, id], {
        cwd: root,
        encoding: "utf-8",
      });
      const lastSeen = gitLog.stdout?.split("\n")[0] || "never referenced in git";
      return `<tr><td>${id}</td><td style="font-family:monospace;font-size:12px">${lastSeen}</td></tr>`;
    })
    .join("");

  panel.webview.html = `<!DOCTYPE html><html><body>
<h2>Policy Staleness</h2>
<table border="1" cellpadding="6" style="border-collapse:collapse">
<tr><th>ID</th><th>Last git reference</th></tr>
${rows}
</table>
</body></html>`;
}

